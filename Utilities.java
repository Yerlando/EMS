	// Method gets comfort temperature from an array based on timestep
	// Returns a comfort temperature value
	public static double getComfortTemperature(int timestep){
		double temperature;
		
		// weekdays
		if(getDayType(timestep) == 1){
			switch(checkDayHour(timestep)){
				
				// 6:30-9:00 and 16:00-0:00
				case 1:
					temperature = 19.0;
					break;
				
				// 9:00-16:00
				case 2:
					temperature = 16;
					break;
				
				// night
				case 3:
					temperature = 17;
					break;
				default: 
					temperature = 0;
			}	
		}
		// weekends
		else
		{
			temperature = 20.0;
		}
		
		return temperature;
	}
	
	// Method gets energy price from an array based on timestep
	// Returns energy price value
	public static double getPrice(int timestep) {
		double price;
		int remainderHour = timestep%96;
		
		// night from 23:00 till 08:00
		if(remainderHour < 32 || remainderHour >= 92)
		{
			price = 0.09;
		}
		// peak period from 17:00 till 19:00
		else if(remainderHour < 76 || remainderHour >= 68){
			//weekends
			if(getDayType(timestep) == 0){
				price = 0.38;
			}
			else{
				price = 0.125;
			}
		}
		else{
			price = 0.125;
		}
		
		return price;
	}
	
	// Method checks a day type: either weekday or weekend
	// Returns an integer that represents the day type
	private static int getDayType(int timestep) {
		int remainderDay = timestep/96+10;
		
		if(remainderDay%7==0 || remainderDay%7==1)
		{
			return 0;
		}
		else{
			return 1;
		}
	}
	
	// Method gets a day hour type
	// Returns an integer that represents the day hour type
	private static int checkDayHour(int timestep) {
		int remainderHour = timestep%96;
		int hour;
		
		//from 06:30 till 09:00 OR from 16:00 till 0:00
		if((remainderHour >= 26 && remainderHour <36) || (remainderHour >= 64 && remainderHour <=95)){
			hour = 1;
		}
		//from 09:00 till 16:00
		else if(remainderHour >= 36 && remainderHour < 64){
			hour = 2;
		}
		// night from 0:00 till 06:30
		else{
			hour = 3;
		}
		
		return hour;
	}
